// Helper to create a horizontal line
export const createLine = (char: string = 'â”€', width: number = 40): string => {
  return char.repeat(width);
};

// Helper to create a centered text with padding
export const centerText = (text: string, width: number = 40, padChar: string = ' '): string => {
  const padding = Math.max(0, Math.floor((width - text.length) / 2));
  return padChar.repeat(padding) + text + padChar.repeat(padding);
};

// Helper to create a box around text
